function numberChecker(number) {
  if (number === 1) {
    console.log('this number is 1')
  } else if (number === 2) {
    console.log('this number is 2')
  } else if (number === 3) {
    console.log('this number is 3')
  } else if (number === 4) {
    console.log('this number is 4')
  } else {
    console.log('this is not my number')
  }
}

numberChecker(3)