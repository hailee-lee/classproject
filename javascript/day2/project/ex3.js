// 기본적인 조건문

if (1 == 1) {
  console.log('number 1 equal number 1 !!!')
}

// if ~ else

if (1 != 1) {
  console.log('number 1 NOT equal number 1 !!!')
} else {
  console.log('number 1 equal number 1 !!!')
}

// if ~ else if

if (1 != 1) {
  console.log('number 1 NOT equal number 1 !!!!')
} else if (1 != 2) {
  console.log('number 1 NOT equal number 2 !!!!')
}

// if ~ else if ~ else

if (1 == 2) {
  console.log('number 1 equal number 2 !!!!')
} else if (1 == 3) {
  console.log('number 1 equal number 3 !!!!')
} else if (1 == 4) {
  console.log('number 1 equal number 4 !!!!')
} else {
  console.log('what ???')
}